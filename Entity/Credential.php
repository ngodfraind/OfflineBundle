<?php

namespace Claroline\OfflineBundle\Entity;

class Credential
{   
    protected $name;
    
    protected $password;
    
    public function getName()
    {
        return $this->name;
    }
    
    public function setName($name)
    {
        $this->name = $name;
    }
    
    public function getPassword()
    {
        return $this->password;
    }
    
    public function setPassword($password)
    {
        $this->password = $password;
    }
}
